# String DataType
name = 'Chirag Shahani'
# Prints complete string
print('My name is', name,type(name))
# Prints 1st character of string
print(name[0])
'''Prints character starting form 3rd to 5th or prints character starting from index 2 to5'''
print(name[2:5])
print(name[2:]) # Prints string character starting from 3rd or index 2
print(name * 2)# Prints string 2 times
print(name + "R")# Prints concatenated string
print(name,"R")
